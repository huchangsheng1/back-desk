<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">
      <div class="title-container">
        <h3 class="title">桃李教育管理系统</h3>
      </div>

      <el-form-item prop="phone">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="phone"
          v-model="loginForm.phone"
          placeholder="phone"
          name="phone"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">登入</el-button>
    </el-form>

  </div>
</template>

<script>
import { validUsername } from '@/utils/validate'
import {login} from '@/api/user'

export default {
  name: 'Login',
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!validUsername(value)) {
        callback(new Error('Please enter the correct user name'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        phone: '',
        password: ''
      },
      loginRules: {
        phone: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      let data = this.loginForm;
      let reg = /^1[3-9][0-9]{9}$/;
      if (!reg.test(data.phone)) {
        this.$notify.error({
              title: '失败 ',
              message: '请输入账号密码',
              type: 'warning'
        });
        return;
      }
        login(data).then(res => {
          
          if (res.code == 200 && res.data.rid !=4) {            
             this.$notify({
              title: '登入成功',
              message: '欢迎使用桃李教育',
              type: 'success'
            });
             //跳转到登入页
             this.$router.push({
               path:'/',
             });
          }else if(res.code == 505) {
              this.$notify.error({
              title: '失败 ',
              message: '请填写正确的手机号',
              type: 'warning'
            });
          }else if(res.code ==504){
            this.$notify.error({
            title: '失败',
            message: '手机或密码错误',
            type: 'warning'
          });
          }else if(res.code == 200 && res.data.rid == 4) {
            this.$notify.error({
              title: '失败 ',
              message: '请输入账号密码',
              type: 'warning',
            });
             localStorage.removeItem('User-Token');
             window.location.href = 'http://hcs998.top'
          }
        })
    },
    }
}
</script>

